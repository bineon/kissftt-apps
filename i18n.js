// i18n engine
function detectLanguage() {
  const stored = localStorage.getItem('kissftt-lang');
  if (stored && TRANSLATIONS[stored]) return stored;

  const nav = navigator.language || navigator.userLanguage || 'en';
  // Exact match first
  if (TRANSLATIONS[nav]) return nav;
  // Try zh variants
  if (nav.startsWith('zh')) {
    if (nav.includes('TW') || nav.includes('HK') || nav.includes('Hant')) return 'zh-TW';
    return 'zh-CN';
  }
  // Base language match
  const base = nav.split('-')[0];
  if (TRANSLATIONS[base]) return base;
  return 'en';
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  localStorage.setItem('kissftt-lang', lang);
  applyTranslations(lang);
}

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;

  // Set dir and lang on html
  const html = document.documentElement;
  html.lang = lang;
  if (RTL_LANGUAGES.includes(lang)) {
    html.dir = 'rtl';
  } else {
    html.dir = 'ltr';
  }

  // Translate all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Translate elements with data-i18n-html (for links inside text)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Update page title
  if (t._pageTitle) {
    document.title = t._pageTitle;
  }

  // Update language selector button text
  const langBtn = document.querySelector('.lang-btn-text');
  if (langBtn && LANGUAGES[lang]) {
    langBtn.textContent = LANGUAGES[lang];
  }

  // Mark active language
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

function initI18n() {
  const lang = detectLanguage();

  // Build language dropdown
  const dropdown = document.querySelector('.lang-dropdown');
  if (dropdown) {
    dropdown.innerHTML = '';
    Object.entries(LANGUAGES).forEach(([code, name]) => {
      const btn = document.createElement('button');
      btn.className = 'lang-option';
      btn.dataset.lang = code;
      btn.textContent = name;
      btn.addEventListener('click', () => {
        setLanguage(code);
        dropdown.classList.remove('open');
      });
      dropdown.appendChild(btn);
    });
  }

  // Toggle dropdown
  const langBtn = document.querySelector('.lang-btn');
  if (langBtn) {
    langBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      dropdown.classList.toggle('open');
    });
    document.addEventListener('click', () => {
      dropdown.classList.remove('open');
    });
  }

  applyTranslations(lang);
}

document.addEventListener('DOMContentLoaded', initI18n);
